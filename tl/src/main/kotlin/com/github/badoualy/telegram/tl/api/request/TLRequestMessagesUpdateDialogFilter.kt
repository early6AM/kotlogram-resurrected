class TLRequestMessagesUpdateDialogFilter() : TLMethod<TLBool>() {
    var id: Int = 0

    var filter: TLAbsDialogFilter? = null

    private val _constructor: String = "messages.updateDialogFilter#1ad4a04a"

    override val constructorId: Int = CONSTRUCTOR_ID

    constructor(id: Int, filter: TLAbsDialogFilter?) : this() {
        this.id = id
        this.filter = filter
    }

    protected override fun computeFlags() {
        _flags = 0
        updateFlags(filter, 1)
    }

    @Throws(IOException::class)
    override fun serializeBody(tlSerializer: TLSerializer) = with (tlSerializer)  {
        computeFlags()

        writeInt(_flags)
        writeInt(id)
        doIfMask(filter, 1) { writeTLObject(it) }
    }

    @Throws(IOException::class)
    override fun deserializeBody(tlDeserializer: TLDeserializer) = with (tlDeserializer)  {
        _flags = readInt()
        id = readInt()
        filter = readIfMask(1) { readTLObject<TLAbsDialogFilter>() }
    }

    override fun computeSerializedSize(): Int {
        computeFlags()

        var size = SIZE_CONSTRUCTOR_ID
        size += SIZE_INT32
        size += SIZE_INT32
        size += getIntIfMask(filter, 1) { it.computeSerializedSize() }
        return size
    }

    override fun toString() = _constructor

    override fun equals(other: Any?): Boolean {
        if (other !is TLRequestMessagesUpdateDialogFilter) return false
        if (other === this) return true

        return _flags == other._flags
                && id == other.id
                && filter == other.filter
    }
    companion object  {
        const val CONSTRUCTOR_ID: Int = 0x1ad4a04a.toInt()
    }
}
